import "./globals.css";
import { type Metadata } from "next";
import { NextIntlClientProvider } from "next-intl";
import { getLocale, getMessages } from "next-intl/server";

import { Providers } from "@/app/providers";
import { getNonce } from "@/shared/server/security/get-nonce";
import { ThemeScript } from "@/shared/theme/theme-script";
import { getServerTheme } from "@/shared/theme/theme.server";

import { RootErrorBoundaryProvider } from "./root-error-boundary";

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default async function RootLayout({ children }: { readonly children: React.ReactNode }) {
  const nonce = await getNonce();
  const initialTheme = await getServerTheme();
  const locale = await getLocale();
  const messages = await getMessages();

  const brand = "default";

  return (
    <html
      suppressHydrationWarning
      lang={locale}
      dir={locale === "ar" ? "rtl" : "ltr"}
      data-brand={brand}
      className={initialTheme === "dark" ? "dark" : undefined}
    >
      <body suppressHydrationWarning>
        <ThemeScript nonce={nonce} />
        <RootErrorBoundaryProvider>
          <NextIntlClientProvider locale={locale} messages={messages}>
            <Providers initialTheme={initialTheme}>{children}</Providers>
          </NextIntlClientProvider>
        </RootErrorBoundaryProvider>
      </body>
    </html>
  );
}
